let num1 = 9.49;

console.log(Math.floor(num1)); // Arredonda para baixo
console.log(Math.ceil(num1)); // Arredonda para cima
console.log(Math.round(num1)); // Arredonda para o mais próximo
console.log(Math.max(1, 2, 3, 4, 5, -10, -50, 1500, 9, 8, 7, 6)); // Retorna o maior número
console.log(Math.min(1, 2, 3, 4, 5, -10, -50, 1500, 9, 8, 7, 6)); // Retorna o menor número
console.log(Math.round(Math.random() * (10 - 5) + 5)); // Retorna um número aleatório entre 10 e 5
